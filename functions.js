// Regular function
function sayHello() {
  console.log('Hello World!');
}
sayHello();

// Anonymous function 
var sayanonhello= function() {
  console.log('Hello World!');
}
sayanonhello();

// Arrow function
var sayarrowhello= () => {
  console.log('Hello World!');
}
sayarrowhello();

// Function expression